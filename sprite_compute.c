#include "cub3d.h"

void	sprite_compute(t_map *map)
{
	int		i;

	i = 0;
	while (map->map_buf[i] != '\0')
	{
		if (map->map_buf[i] == '2')
			map->sprite_cnt++;
		i++;
	}
}
