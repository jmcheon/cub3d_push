#include "libft.h"

void				*ft_memcpy(void *dst, const void *src, size_t size)
{
	size_t			i;
	unsigned char	*dst2;
	unsigned char	*src2;

	i = 0;
	dst2 = (unsigned char *)dst;
	src2 = (unsigned char *)src;
	if (!size || dst == src)
		return (dst);
	while (i < size)
	{
		dst2[i] = src2[i];
		i++;
	}
	return (dst);
}
