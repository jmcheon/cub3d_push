#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*temp;

	temp = lst;
	while (temp != NULL && f != NULL)
	{
		f(temp->content);
		temp = temp->next;
	}
}
